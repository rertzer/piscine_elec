#include "ATmega.h"

void	init_mode4()
{
	/* set led as output */
	DDRD |= 1 << PD3 | 1 << PD5 | 1 << PD6;
	PORTD &= ~(1 << PD3 | 1 << PD5 | 1 << PD6);

	spi_init();
}

