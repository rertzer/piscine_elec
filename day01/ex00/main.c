#include <avr/io.h>

int	main(void)
{
	DDRB |= 1 << PB0;
	PORTB &= ~(1 << PB0);

	while (1)
	{
		for (uint32_t i = 0; i < 1300000; ++i);
		PORTB ^= (1 << PB0);
	}
}
